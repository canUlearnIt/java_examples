package composition;

public class Body {
	
	private String type;
	private int numOfLegs;
	
	
	
	public Body(String type, int numOfLegs) {
		super();
		this.type = type;
		this.numOfLegs = numOfLegs;
	}
	
	
	public String getType() {
		return type;
	}
	public int getNumOfLegs() {
		return numOfLegs;
	}
	
	
	

}
